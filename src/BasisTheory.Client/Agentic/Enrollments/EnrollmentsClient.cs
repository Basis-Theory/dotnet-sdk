    /// <summary>
    /// List all enrollments for the current tenant with cursor-based pagination.
    /// </summary>
    /// <example><code>
    /// await client.Agentic.Enrollments.ListAsync(new EnrollmentsListRequest());
    /// </code></example>
    public async Task<Pager<Enrollment>> ListAsync(
        EnrollmentsListRequest request,
        RequestOptions? options = null,
        CancellationToken cancellationToken = default
    )
    {
        if (request is not null)
        {
            request = request with { };
        }
        var pager = await CursorPager<
            EnrollmentsListRequest,
            RequestOptions?,
            EnrollmentList,
            string?,
            Enrollment
        >
            .CreateInstanceAsync(
                request,
                options,
                ListInternalAsync,
                (request, cursor) =>
                {
                    request.Cursor = cursor;
                },
                response => response?.Pagination?.NextCursor,
                response => response?.Data?.ToList(),
                cancellationToken
            )
            .ConfigureAwait(false);
        return pager;
    }

