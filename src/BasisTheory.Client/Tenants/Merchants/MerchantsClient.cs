        }
        if (request.Size != null)
        {
            _query["size"] = request.Size.Value.ToString();
        }
        var response = await _client
            .SendRequestAsync(
                new JsonRequest
                {
                    BaseUrl = _client.Options.BaseUrl,
                    Method = HttpMethod.Get,
                    Path = string.Format(
                        "tenants/{0}/merchants",
                        ValueConvert.ToPathParameterString(tenantId)
                    ),
